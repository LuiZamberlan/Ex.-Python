from math import cos, tan, sin, radians

ang = float(input('Digite o valor do ângulo: '))

#Função sin() calcula o seno com o valor do ângulo recebido
print(f'O seno do ângulo de {ang} é {sin(radians(ang)):.2f}')

#Função cos() calcula o cosseno com o valor do ângulo recebido
print(f'O seno do ângulo de {ang} é {cos(radians(ang)):.2f}')

#Função tan() calcula a tangente com o valor do ângulo recebido
print(f'O seno do ângulo de {ang} é {tan(radians(ang)):.2f}')

#Função radians() transforma o valor do ângulo em valor radiano
